# Aula 5 - Operadores Lógicos

# Atribuição em variaveis:

nota = 4
print("nota: ",nota)

media = 6
print("media: ",media)

# Comparações

print("5 é igual a 5? :",5 == 5)

print("nota é >= media :", nota >= media)

print("nota é < media :",nota < media)

print("nota é != 4 :",nota != 4)

print("nota está contido no vetor 3,4,5? :",nota in [3,4,5]) #Está contido no range

print("nota não está contido no vetor 3,4,5,6? :",nota not in [3,4,5,6]) # Não está contido

print("nota está contido em 1,2,3,5,6? :",nota in [1,2,3,5,6])

print("nota é 5? :",nota is 5 )# 4 é 6?
