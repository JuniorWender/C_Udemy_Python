# aula de string 3 - Fatiamento de string

nome="Jorge Wender"

print(nome[0:5]) # Assim ele irá começar do indice 0 até o 5 , portanto pegando do "J" até o "E"

print(nome[6:12]) # Printa somente wender

print(nome[:10]) # Se não colocar nada antes dos : ele assumirá que o primeiro indice é 0 e irá imprimir: Jorge Wend

print(nome[5:]) # Colocando sem o intervalo final, ele irá fazer tudo do indice definido no inicio até o tamanho total